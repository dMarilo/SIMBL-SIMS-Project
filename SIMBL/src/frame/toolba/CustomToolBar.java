package frame.toolba;

import javax.swing.JToolBar;

import frame.toolbar.tools.ToolsForProjekat;
import frame.toolbar.tools.ToolsForSimulacija;
import frame.toolbar.tools.ToolsForUredjivanjeDijagrama;
import frame.toolbar.tools.ToolsForUredjivanjeSvojstvaElementa;
import frame.toolbar.tools.ToolsForZoom;


public class CustomToolBar extends JToolBar{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	public CustomToolBar () {
		add(new ToolsForProjekat());
		addSeparator();
		add(new ToolsForUredjivanjeDijagrama());
		addSeparator();
		add(new ToolsForUredjivanjeSvojstvaElementa());
		addSeparator();
		add(new ToolsForSimulacija());
		addSeparator();
		add(new ToolsForZoom());
	}

}
